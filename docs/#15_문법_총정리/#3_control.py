#-- if문
# ● 조건식을 평가하고 참인 경우만 구문이 수행
# ● 2개 이상의 구문은 들여쓰기로 블록을 지정
#   - 함수와 동일
#   - 들여쓰기의 정도는 파일 전체를 통틀어 일치해야 함

#-- while문
#   [조건식이 참(True) 인 동안 내부 구문을 반복 수행]
# ● 조건식은 구문이 수행되기 이전에 우선평가
# ● 구문을 모두 수행 이후 다시 조건식을 재평가
# ● 조건식이 거짓(False)이면 while문 구조를 벗어남

#-- for문 (조건식이 참(True) 인 동안 내부 구문을 반복 수행)
#   [시퀀스형 객체를 순차적으로 순회]
# ● '시퀀스형 객체 S'의 각 아이템을 '아이템I'에 할당
# ● 할당된 아이템I를 가지고 구문을 수행
# ● 모든 아이템을 순회하거나 break를 만나면 for문이 종료

